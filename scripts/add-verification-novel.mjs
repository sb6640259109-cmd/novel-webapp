import dotenv from 'dotenv';
import { FieldValue } from 'firebase-admin/firestore';
import { getFirestoreDatabase } from '../lib/firebase-admin.js';

dotenv.config({ path: '.env', quiet: true });
dotenv.config({ path: '.env.local', override: false, quiet: true });

const id = 'the-clockmaker-of-lost-time';
const novel = {
  title: 'ช่างนาฬิกาแห่งเวลาที่สูญหาย',
  author: 'อิงดาว ณ ราตรี',
  genre: 'แฟนตาซี',
  description: 'เด็กหนุ่มพบร้านนาฬิกาที่ซ่อมคืนได้ไม่ใช่เวลา แต่เป็นช่วงชีวิตที่ผู้คนทำหาย และต้องตัดสินใจว่าจะย้อนคืนวันที่สูญเสียแม่ หรือช่วยทั้งเมืองจากการหยุดนิ่งตลอดกาล',
  content: `ในตรอกที่ไม่มีชื่อ มีร้านนาฬิกาเปิดเฉพาะคืนที่ฝนตก ผู้คนเล่าว่าช่างชราในร้านสามารถซ่อมเวลาที่สูญหายได้ เพียงนำสิ่งของจากวันที่อยากได้คืนมาแลก

ภีมพบร้านนั้นหลังเก็บกล่องดนตรีของแม่ได้จากห้องใต้หลังคา เขาอยากย้อนกลับไปยังเช้าวันสุดท้ายที่ไม่ได้กล่าวลา ช่างชรารับกล่องไว้ แต่เตือนว่าทุกนาทีที่คืนให้คนหนึ่ง จะถูกดึงออกจากเวลาของคนอื่น

ก่อนภีมจะตัดสินใจ นาฬิกาทุกเรือนในเมืองหยุดพร้อมกัน ผู้คนค้างอยู่กลางถนน สายฝนลอยนิ่งในอากาศ และเงาขนาดใหญ่เริ่มกลืนกินเข็มนาฬิกาบนหอคอย ช่างชราเผยว่าเวลาที่ผู้คนเสียดายสะสมจนกลายเป็นสัตว์ร้าย ซึ่งจะทำให้เมืองติดอยู่ในอดีตตลอดไป

ภีมถือกล่องดนตรีขึ้นหอคอย เขาสามารถใช้พลังของมันย้อนคืนวันสุดท้ายกับแม่ หรือปล่อยความทรงจำนั้นเพื่อทำให้นาฬิกาของทั้งเมืองเดินต่อ เขาหลับตา ฟังทำนองที่แม่เคยเปิด แล้ววางกล่องลงในหัวใจของเครื่องจักร

เข็มวินาทีขยับอีกครั้ง สายฝนตกถึงพื้น ผู้คนก้าวเดินต่อโดยไม่รู้ว่าเคยหยุดนิ่ง ภีมจำเสียงของแม่ไม่ได้ชัดเหมือนเดิม แต่เขาจำความอบอุ่นในวันนั้นได้ และเข้าใจว่าเวลาที่รักไม่จำเป็นต้องย้อนกลับมา จึงจะมีความหมาย

รุ่งเช้า ร้านนาฬิกาในตรอกหายไป เหลือเพียงป้ายเล็กเขียนว่า “เวลาที่ซ่อมดีที่สุด คือเวลาที่เรายังมีอยู่” ภีมเก็บป้ายนั้นไว้ และเริ่มใช้ทุกวันโดยไม่รอให้ต้องย้อนกลับไปแก้ไขอีก`,
  rating: 4.6,
  image: null,
  updatedAt: FieldValue.serverTimestamp(),
};

const reference = getFirestoreDatabase().collection('novels').doc(id);
const existing = await reference.get();
await reference.set({
  ...novel,
  createdAt: existing.exists ? existing.data().createdAt : FieldValue.serverTimestamp(),
}, { merge: true });
console.log(JSON.stringify({ success: true, id, title: novel.title, created: !existing.exists }));
